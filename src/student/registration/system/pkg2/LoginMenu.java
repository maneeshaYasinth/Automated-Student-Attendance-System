    private boolean verifyLoginDetails(String userName,String password){
        User[] userArray = Data.getUserArray();
        for (int i = 0; i < userArray.length; i++) {
            if(userArray[i].getUsername().equals(userName) && userArray[i].getPassword().equals(password)) return true;
        }
        return false;
    }
